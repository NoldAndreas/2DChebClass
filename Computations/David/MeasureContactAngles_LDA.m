                      'y2Min',0,'y2Max',2);
                                                        
	V2Num   = struct('Fex','SplitDisk','L',2,'L2',1.,'N',[40,40]);
        
    optsNum = struct('PhysArea',Phys_Area,...
                     'PlotArea',Plot_Area,'SubArea',Sub_Area,...
                     'plotTimes',0:0.1:6,...
